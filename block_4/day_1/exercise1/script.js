function add(v1, v2) {
  return v1 + v2;
}

function subtract(v1, v2) {
  return v1 - v2;
}

function multiply(v1, v2) {
  return v1 * v2;
}

function divide(v1, v2) {
  return v1 / v2;
}

function absolute(v1) {
  return Math.abs(v1);
}

console.log(add(5, 5));
console.log(subtract(5, 5));
console.log(divide(5, 5));
console.log(multiply(5, 5));
console.log(absolute(-5));